
public class MagnitudeCharge {
	private double _magnitude;
	private double _charge;
	
	public MagnitudeCharge(double magnitude, double charge)
	{
		_magnitude=magnitude;
		_charge=charge;
	}
	
	public double getMagnitude()
	{
		return _magnitude;
	}
	
	public double getCharge()
	{
		return _charge;
	}
}
